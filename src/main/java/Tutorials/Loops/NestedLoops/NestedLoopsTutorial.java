package Tutorials.Loops.NestedLoops;

public class NestedLoopsTutorial {
    public static void main(String[] args) {
        
        // 1.
        for (int j = 1; j <= 3; j++) {
            for (int i = 1; i <= 9; i++) {
                System.out.print(i + " ");
            }
            System.out.println();
        }

    }
}
